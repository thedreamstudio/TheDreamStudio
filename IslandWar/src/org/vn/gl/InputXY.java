package org.vn.gl;

public class InputXY {
	public static final int DISABLE = -9999;

	public InputXY() {
		x = DISABLE;
		y = 0;
	}

	public float x, y;

	public float getX() {
		return x;
	}

	public float getY() {
		return y;
	}

	public boolean isDisable() {
		return x == DISABLE;
	}

	public void disable() {
		x = DISABLE;
	}
}
